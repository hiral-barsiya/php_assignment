<?php
// Get the current date in different formats

// Y-m-d format (Year-Month-Day)
echo "Current date in Y-m-d format: " . date("Y-m-d") . "<br>";

// d/m/Y format (Day/Month/Year)
echo "Current date in d/m/Y format: " . date("d/m/Y") . "<br>";

// l format (Full textual representation of the day of the week)
echo "Current day of the week: " . date("l") . "<br>";

// F jS Y format (Full textual month, day with suffix, and year)
echo "Current date in F jS Y format: " . date("F jS Y") . "<br>";

// m/d/Y format (Month/Day/Year)
echo "Current date in m/d/Y format: " . date("m/d/Y") . "<br>";

// H:i:s format (24-hour format of hour, minute, and second)
echo "Current time in H:i:s format: " . date("H:i:s") . "<br>";



?>
